namespace Steel
{
    /// <summary>
    /// Type of content text can support
    /// </summary>
    public enum TextType
    {
        /// <summary>
        /// No validation
        /// </summary>
        Standard,
        
        /// <summary>
        /// Integer number
        /// </summary>
        IntegerNumber,
        
        /// <summary>
        /// Float
        /// </summary>
        DecimalNumber,
        
        /// <summary>
        /// Letters and numbers
        /// </summary>
        Alphanumeric,
    }
}